    window.RGBToHtmlColor = function RGBToHtmlColor(rgb) {
        return `rgba(${rgb.r}, ${rgb.g}, ${rgb.b}, ${rgb.alpha / 255})`;
    };
    window.rplaceHexToRGB = function rplaceHexToRGB(hexColor) {
        return {
            alpha: (hexColor >> 24) & 0xff,
            b: (hexColor >> 16) & 0xff,
            g: (hexColor >> 8) & 0xff,
            r: (hexColor >> 0) & 0xff,
        };
    };
